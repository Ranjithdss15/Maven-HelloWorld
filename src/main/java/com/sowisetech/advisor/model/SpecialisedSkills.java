package com.sowisetech.advisor.model;

public class SpecialisedSkills {

	private long specialisedSkillId;
	private String specialisedSkill;

	
	public long getSpecialisedSkillId() {
		return specialisedSkillId;
	}

	public void setSpecialisedSkillId(long specialisedSkillId) {
		this.specialisedSkillId = specialisedSkillId;
	}

	public String getSpecialisedSkill() {
		return specialisedSkill;
	}

	public void setSpecialisedSkill(String specialisedSkill) {
		this.specialisedSkill = specialisedSkill;
	}

}
